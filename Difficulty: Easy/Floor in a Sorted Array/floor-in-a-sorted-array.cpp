class Solution {
  public:

    int findFloor(vector<int>& arr, int k) {

        // Your code here
        if(arr[0] > k){
            return -1;
        }
        int n = arr.size()-1;
        for(int i=n;i>0;i--){
            if(arr[i] <= k){
                return i;
            }
        }
        return 0;
    }
};


