class Solution {
  public:
    // Function to find maximum product subarray
    int maxProduct(vector<int> &arr) {
        // Your Code Here
        int n = arr.size();
        int mx = arr[0];
        int mn = arr[0];
        int ans = arr[0];
        
        for(int i=1;i<n;i++){
           int t = arr[i];
           
           if(t < 0){
               swap(mx,mn);
           }
           
           mx = max(t,mx*t);
           mn = min(t,mn*t);
           ans = max(ans,mx);
        }
        return ans;
    }
};

